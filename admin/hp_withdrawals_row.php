<?php 
	include '../inc/session.php';

	if(isset($_POST['id'])){
		$id = $_POST['id'];
		
		$conn = $pdo->open();

		$stmt = $conn->prepare("SELECT *, hp_transactions.id AS wid, hp_transactions.username AS username, hp_transactions.payment_mode AS payment FROM hp_transactions WHERE hp_transactions.id=:id");
		$stmt->execute(['id'=>$id]);
		$row = $stmt->fetch();
		
		$pdo->close();

		echo json_encode($row);
	}
?>